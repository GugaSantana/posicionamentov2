      <p><b>4 - COMO USAR O ChatGPT</b></p>
      
      <p>Neste capítulo apresentaremos, passo a passo, como interagir da maneira mais produtiva com o ChatGPT de Inteligência Artificial com o objetivo de extrair os melhores resultados de suas respostas.</p>
      <p>Dividimos este capítulo em duas seções: <b>SOBRE O QUE PERGUNTAR</b> e <b>COMO PERGUNTAR.</b></p>
      
      
      <br>
      <p><b>4.1- Sobre O QUE perguntar</b></p>

      <p>Esta é uma questão fundamental que devemos levar em consideração para não perder o foco, ou seja, para não nos dispersar e, em consequência perder qualidade nas respostas.</p>
      <p>Qual é o nosso foco, nosso objetivo com o ChatGPT?</p>
      <p><i>"Nosso objetivo com o ChatGPT é produzir os melhores Planos de Ação para melhorar nosso desempenho nas 20 competências da metodologia TOPSELLER para nos habilitar como profissionais de Vendas Consultivas de maneira rápida e eficaz"</i></p>
      <p>Para garantir nosso Foco e nossa Produtividade, as perguntas devem estar fundamentadas em duas dimensões: o <b>DIAGNÓSTICO</b> e as <b>RECOMENDAÇÕES</b> que foram apresentadas em cada um dos 20 Instrumentos da metodologia TOPSELLER.</p>
      <p><i>- PERGUNTAS BASEADAS NO DIAGNÓSTICO</i></p>
      <p>Cada Instrumento apresentou um <b>DIAGNÓSTICO</b> ANALÍTICO QUANTITATIVO E QUALITATIVO relativo à competência em pauta no Instrumento.</p>
      <p>Lembramos que um Diagnóstico é um desvio entre uma Situação Atual e uma Situação Ideal que se pretende alcançar.</p>
      <p>Então, as perguntas que formularemos ao ChatGPT serão baseadas nos desvios apontados nos diagnósticos.</p>
      <p>Esta será a “matéria prima” das perguntas que faremos ao ChatGPT.</p>
      <p>Como formular as perguntas da maneira mais eficaz será o tema do próximo ponto 4.2</p>
      <p><i>- PERGUNTAS BASEADAS NAS RECOMENDAÇÕES FEITAS</i></p>
      <p>Lembramos que nos resultados dos Diagnósticos foram fundamentadas as <b>RECOMENDAÇÕES</b> com o objetivo de eliminar os desvios ou hiatos entre a Situação Atual e a Situação Ideal em relação a cada uma das 20 competências apresentadas.</p>
      <p>Todas as Recomendações do TOPSELLER são precisas e personalizadas, pois foram formuladas a partir dos resultados individuais que os participantes deram aos instrumentos de captura de informações (questionários, escalas, matrizes), conforme os diferentes instrumentos.</p>
      <p>As Recomendações representam <b>O QUE</b> devemos fazer para melhorar nosso desempenho, para eliminar o desvio e assim, aumentar nossa produtividade, nossa eficácia e nosso faturamento.</p>
      <p>Cada Instrumento apresenta Recomendações analíticas e precisas.</p>
      <p>Em síntese, as perguntas que faremos ao ChatGPT serão baseadas nas informações e no conteúdo dos Diagnósticos e nas Recomendações de cada Instrumento.</p>
      

      <br>
      <p><b>4.2- COMO perguntar</b></p>

      <p>É importante lembrar que o ChatGPT é um chat conversacional, ou seja, INTERATIVO, de modo que através de perguntas e respostas estabelecemos um "diálogo" com a Inteligência Artificial.</p>
      <p>Através deste diálogo iremos detalhando, aprofundando e refinando as respostas que recebemos da Inteligência Artificial.</p>
      <p>As respostas que receberemos do ChatGPT, constituirão o conteúdo dos Planos de Ação do TOPSELLER.</p>
      <p>Vamos agora nos referir de maneira detalhada aos cuidados que deveremos ter ao formular as perguntas para obter as melhores respostas da Inteligência Artificial, as respostas mais objetivas e precisas.</p>
      <p><i><b>Quanto melhor formuladas sejam as perguntas melhor serão as respostas!</b></i></p>
      <p>As perguntas que formulamos ao ChatGPT são chamadas de "PROMPTS". Os Prompts, de fato, são perguntas, mas tem valor de instruções ou de ordens que estamos dando ao ChatGPT.</p>
      <p>Temos que "saber falar" com a Inteligência Artificial para obter resultados excelentes.</p>
      <p>Estas são as recomendações que pedimos sejam atendidas pelos participantes ao fazer seus Prompts:<br>
      - Utilizar palavras precisas nas solicitações e evitar ambiguidades.<br>
      - Utilizar palavras-chave, palavras -sínteses.<br>
      - Evitar palavras genéricas.<br>
      - Utilizar uma linguagem adequada, formal, evitar gírias, que poderão não ser reconhecidas.<br>
      - Perguntar sobre uma coisa ou questão de cada vez. Ter um propósito específico para cada pergunta.<br>
      - Se a resposta não for satisfatória, reformular a pergunta utilizando outras palavras ou outra perspectiva.<br>
      - Fazer as perguntas numa ordem lógica para facilitar o processamento e a articulação com perguntas anteriores.<br>
      - Dar as instruções de forma clara e linear, não ser redundante no conteúdo.<br>
      - Podemos pedir respostas estruturadas, por exemplo, podemos solicitar que o ChatGPT nos indique 5 coisas específicas para fazer, por ordem de importância.<br>
      - Como o ChatGPT é um chat conversacional, é interessante formular novas perguntas a partir da resposta anterior, por exemplo, pedir para detalhar mais sobre uma parte da resposta, nos referindo a um determinado parágrafo em particular.<br>
      <p>Você deve imaginar que está <b>dialogando com um superespecialista</b> que está nas melhores condições de elaborar os mais excepcionais Planos de Ação para implementar as Recomendações do TOPSELLER.</p></p>
      
