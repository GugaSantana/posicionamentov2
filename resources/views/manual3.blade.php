<h3 style="color: #35408f">3 - ETAPAS DA METODOLOGIA E INSTRUMENTOS DE CADA ETAPA</h3>
<br>

<p>Este Projeto e esta Metodologia permitem aumentar suas Vendas por Objetivos. Isto se dá através da transferência digital e à distância, com 20 Instrumentos quantitativos, altamente estruturados e objetivos. À Metodologia também contempla a formulação de Planos de Ação, também estruturados e de simples resolução, para aumentar as Vendas, com Metas Definidas de Desempenho e Avaliação do Progresso da sua implementação.</p>
<p>O Projeto está estruturado em 3 etapas:</p>
<p>A - Precondições para aumentar as Vendas.</p>
<p>B - Como aumentar as Vendas através das Técnicas de Vendas Consultivas.</p>
<p>C - Gestão das Vendas utilizando esta Metodologia.</p>
<p>Estas três etapas são agora descritas para você conhecer os temas abordados e contextualizar os Instrumentos que você utilizará em cada uma destas etapas para aumentar suas Vendas.</p>
<br>
<p style="color: #35408f; font-size: 21px"><b>A - PRECONDIÇÕES PARA AUMENTAR AS VENDAS</b></p>

<p style="color: #ef6502">- OBJETIVOS DA ETAPA</p>
<p style="color: #ef6502">Otimizar os fatores-chave para aumentar a Produtividade das Vendas.</p>
<p>Uma série de questões devem ser consideradas para otimizar os resultados da implementação das Vendas Consultivas. Isto antes da apresentação de suas técnicas.</p>
<p>São variáveis ou fatores que devem ser gerenciados para obter os melhores resultados.</p>
<p>Esta etapa inclui os primeiros 6 Instrumentos da Metodologia que estão orientados a aumentar sua Produtividade em Vendas, independentemente das Vendas Consultivas. O objetivo destes seis Instrumentos é aumentar sua base de Produtividade.</p>
<p>São de fato precondições que devem ser desenvolvidas e nas quais você deve melhorar para aumentar sua Produtividade e otimizar, a seguir, a prática específica de Vendas Consultivas.</p>
<p>A eficácia dos Instrumentos de Vendas Consultivas que será colocada em prática na etapa seguinte da Metodologia, estará diretamente relacionada à melhoria destas condições que você operacionalizará com estes 6 Instrumentos:</p>
<p>1 - Meu Nível de Empreendedorismo.</p>
<p>2 - Meus maiores problemas para Aumentar as Vendas.</p>
<p>3 - Meu perfil como Profissional de Vendas.</p>
<p>4 - Meu conhecimento da linha de produtos que vendo.</p>
<p>5 - Meu conhecimento dos produtos concorrentes.</p>
<p>6 - Matriz para aumentar minha Produtividade nas Vendas.</p>
<br>
<p style="color: #35408f; font-size: 21px"><b>B - COMO AUMENTAR AS VENDAS ATRAVÉS DAS TÉCNICAS DE VENDAS CONSULTIVAS</b></p>

<p style="color: #ef6502">- OBJETIVOS DA ETAPA</p>
<p style="color: #ef6502">Desenvolver sistematicamente à distância e por meio digital, as Competências que operacionalizam a Metodologia das Vendas Consultivas, através de Instrumentos estruturados que permitem fazer uma avaliação quantitativa dos resultados.</p>
<p>Nesta etapa, após otimizadas as precondições para aumentar as Vendas, serão transferidas para você as técnicas das Vendas Consultivas, através de 11 Instrumentos específicos que foram organizados em 6 grupos, para atender as etapas da Metodologia das Vendas Consultivas.</p>
<p>Os objetivos desses 6 grupos e dos Instrumentos que os compõem são os seguintes:</p>
<br>
<p><b>1 - O Gerenciamento do Campo Psicológico</b></p>
<p>A premissa fundamental das Vendas Consultivas é estabelecer o melhor relacionamento com o cliente.</p>
<p>É condição obrigatória estabelecer um ambiente de empatia com o cliente. Um ambiente agradável, descontraído e de confiança recíproca, para gerar assim as condições comportamentais que permitirão tirar o maior proveito das etapas que operacionalizam as Vendas Consultivas.</p>
<p>Partimos da premissa que quando duas ou mais pessoas se relacionam, se estabelece um “campo psicológico” entre elas. Entendemos que esse “campo psicológico” pode e deve ser gerenciado.</p>
<p>Nesta fase da Metodologia serão transferidas quatro técnicas comportamentais poderosas e reconhecidas mundialmente por seu potencial de contribuição.</p>
<p>No final desta fase do Programa, você terá todas as condições de gerenciar tecnicamente o Campo Psicológico junto ao seu cliente, criando a base fundamental para aplicar com sucesso as técnicas de Vendas Consultivas.</p>
<p>Nenhum programa oferecido no Mercado disponibiliza estas técnicas, sem as quais a eficácia das Vendas Consultivas fica comprometida, porque as variáveis comportamentais não são abordadas em bases técnicas.</p>
<p>Os Quatro Instrumentos desta Fase que lhe serão transferidos permitirão:</p>
<p>7 - Gerenciar o relacionamento com o cliente através dos Estados do Ego.</p>
<p>8 - Gerenciar o relacionamento com o cliente através da Exposição e do Feedback.</p>
<p>9 - Gerenciar o relacionamento com o cliente utilizando os Canais Neurolinguísticos.</p>
<p>10 - Gerenciar o relacionamento com o cliente através de Inteligência Emocional.</p>
<br>
<p><b>2 - Aumento da Produtividade da PROSPECÇÃO de Clientes</b></p>
<p>Sem potenciais clientes interessados nos produtos que você vende, não haverá fechamento de negócios.</p>
<p>Com a Prospecção você identifica pessoas que poderão estar interessadas em seus produtos.</p>
<p>Os dois Instrumentos desta fase habilitarão você a desenvolver com técnicas apropriadas esta primeira e fundamental atividade para aumentar suas vendas. Os Instrumentos são:</p>
<p>11 - Definição do Perfil do Cliente Ideal.</p>
<p>12 - Como fazer a Prospecção de Clientes.</p>
<br>
<p><b>3 - Aumento da Produtividade da QUALIFICAÇÃO de Clientes</b></p>
<p>Depois de ter uma lista de possíveis clientes, você deve estabelecer se eles realmente têm potencial para serem atendidos com os produtos que você comercializa e se tem capacidade de compra.</p>
<p>Para pôr em prática estas duas condições, a Metodologia lhe oferece dois Instrumentos:</p>
<p>13 - A Qualificação dos Clientes Potenciais.</p>
<p>14 - A Matriz para Identificar Clientes Prioritários.</p>
<br>
<p><b>4 - Aumento da Produtividade da APRESENTAÇÃO para os Clientes</b></p>
<p>Após termos os clientes potenciais qualificados, o próximo passo é fazer uma apresentação personalizada e inteligente dos seus produtos, para atender os desejos e necessidades de seus clientes potenciais.</p>
<p>Para isto, é necessário dispor de um <b>Banco de Argumentos</b>, que inclua muitos argumentos de qualidade, justamente para poder personalizar as apresentações e orientar o poder decisório do cliente.</p>
<p>Criar este <b>Banco de Argumentos</b>, a partir de Forças Propulsoras das Vendas que serão apresentadas, é o objetivo do Instrumento 15 desta fase da Metodologia, utilizado para operacionalizar este conceito.</p>
<p>15 - Desenvolvimento da Quantidade e Qualidade dos meus Argumentos de Vendas (Forças Propulsoras).</p>
<br>
<p><b>5 - Aumento da Produtividade da NEGOCIAÇÃO com os Clientes (a Venda como um Campo de Forças)</b></p>
<p>Negociar requer técnicas. O conceito fundamental da Metodologia nesta fase da Venda Consultiva é o de “<b>Campo de Forças</b>”</p>
<p>A apresentação está apoiada nas <b>Forças Propulsoras</b>, porém, na Negociação temos que lidar com <b>Forças Restritivas</b> ou obstáculos e é por isto que surge e se configura o <b>Campo de Forças</b>.</p>
<p>Criado o <b>Campo de Forças</b> entre as <b>Forças Propulsoras</b> e as <b>Forças Restritivas</b>, o Instrumento desta fase o habilitará para que a “resultante de forças” favoreça as <b>Forças Propulsoras</b> e assim você conseguirá a Venda.</p>
<p>As técnicas para uma negociação bem-sucedida lhe serão transferidas pelo Instrumento:</p>
<p>16 - Neutralização dos obstáculos da venda (Forças Restritivas).</p>
<br>
<p><b>6 - Aumento da Produtividade do FECHAMENTO das Vendas</b></p>
<p>Uma Venda Consultiva bem-sucedida faz com que a fase do Fechamento seja fácil, rápida e natural.</p>
<p>Isto porque o vendedor já sabe gerenciar o <b>Campo Psicológico</b> e o <b>Campo de Forças</b>, através de Instrumentos que lhe serão transferidos e que já foram apresentados nos itens acima.</p>
<p>O Instrumento 17 da Metodologia permite inserir estes conceitos na fase do fechamento:</p>
<p>17 - Técnicas de Fechamento de Vendas. </p>
<br>
<p style="color: #35408f; font-size: 21px"><b>C - A GESTÃO DAS VENDAS UTILIZANDO ESTA METODOLOGIA</b></p>
