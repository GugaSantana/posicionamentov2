<h2 style="color: #35408f;text-align: center;margin-top: 10%;">GUIA DO PROGRAMA TOP SELLER<h2>
        
<h3 style="color: #35408f">1 - APRESENTAÇÃO E FUNDAMENTAÇÃO DO PROJETO: PORQUE ESTE PROJETO É FUNDAMENTAL PARA SUA ATIVIDADE PROFISSIONAL</h3>
<br>
<p>Na década de 1990 o mundo ingressou na Quarta Revolução Industrial: uma RUPTURA DE PARADIGMAS.</p>
<p>Nas organizações, como reflexo, essa Ruptura de Paradigmas impõe um novo desafio estratégico em todas as áreas, inclusive na área comercial.</p>
<p>A pergunta é: Como Vender neste novo paradigma?</p>
<p>Com a Primeira e Segunda Revolução Industrial, uma impressionante variedade de novos produtos e serviços foi oferecida ao mercado, a custos cada vez menores, para atender necessidades reprimidas da população mundial.</p>
<p>Naquele contexto era fácil vender, por uma razão muito simples:</p>
<p>A DEMANDA SUPERAVA A OFERTA, A DEMANDA ERA MAIOR QUE A OFERTA.</p>
<p>Por essa razão, Henry Ford desafiou o mercado ao afirmar que “você pode escolher a cor do carro desde que seja preto”, porque ele só produzia carros da cor preta!</p>
<p>Mas já na década de 1960, OFERTA e DEMANDA se equilibraram e os mercados começaram a se saturar.</p>
<p>Então surge uma nova disciplina para ajudar a vender: o Marketing.</p>
<p>Mas apesar das contribuições do Marketing, a equação mudou para sempre e a OFERTA é cada vez maior que a DEMANDA.</p>
<p>O desequilíbrio entre Oferta e Demanda se tornou ainda maior com o advento da Terceira Revolução Industrial, na década de 1970: A REVOLUÇÃO DA INFORMÁTICA. </p>
<p>Como consequência, foi necessário mudar a forma de vender, e começou a transição da prática de <b>Vendas Transacionais</b> para a prática de <b>Vendas Consultivas</b>.</p>
<p>A migração foi lenta por problemas da metodologia de Vendas Consultivas que ainda não estava consolidada e pela resistência cultural e a acomodação das empresas e dos profissionais de vendas.</p>
<p>E chegamos a Quarta Revolução Industrial, a Revolução Digital, com a Internet das coisas, a inteligência artificial, a robótica, o cloud computing, o big data, o 5G... a lista não para por aqui.</p>
<p>Aos mercados saturados se soma um novo desafio para os vendedores: agora os clientes são digitais, a fidelidade dos clientes é cada vez menor e a velocidade das mudanças é cada vez maior. Neste cenário, a profissionalização das equipes de vendas, em novas bases, passa a ser o grande diferencial competitivo para as empresas.</p>
<p>Temos que reduzir o Ciclo das Vendas frente a um novo tipo de cliente mais exigente e informado: O PROFISSIONAL DE VENDAS NECESSITA DE UMA NOVA METODOLOGIA PARA VENDER.</p>
<p>E aqui está o principal desafio dos profissionais de vendas: se impõe uma nova forma de vender: E essa nova forma de vender é o modelo das Vendas Consultivas.</p>
<p>Tomar esta consciência é fundamental para as organizações e para os profissionais de vendas.</p>
<p>Mas aqui surge um outro desafio que é a consistência e a fundamentação técnica do modelo de Vendas Consultivas a ser adotado pelas empresas e pelos profissionais de vendas.</p>
<p>Nossa metodologia, na qual se fundamentam os 20 instrumentos deste programa, apresenta reconhecidas vantagens competitivas, tanto pela sua consistência metodológica como pela possibilidade de sua implementação 100% digital, apoiada em instrumentos estruturados e quantitativos, que garantem resultados objetivos e mensuráveis.</p>
<p>O programa que você fará é o resultado de 32 anos de experiência consultiva e de capacitação, ao longo dos quais a empresa Posicionamento foi atualizando permanentemente a abordagem, e incorporando e operacionalizando as mais inovadoras e confiáveis contribuições a nível mundial.</p>
<p>Agora você participará de um projeto pioneiro, único no mercado, profundamente inovador, todo ele digital e com garantia de desenvolvimento real das competências necessárias para praticar efetivamente Vendas Consultivas.</p>


